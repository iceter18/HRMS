    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        HRMS a = new HRMS();
        JSystemFileChooser fc = new JSystemFileChooser();

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg", "png");
        fc.addChoosableFileFilter(filter);
        fc.setFileFilter(filter);
        fc.setCurrentDirectory(new File("D:\\HRMS Photos"));
        
        if (fc.showOpenDialog(this)==JFileChooser.APPROVE_OPTION) {
            File photo = fc.getSelectedFile();
            path = photo.getAbsolutePath();

            pic.setIcon(a.resizer(path, pic));
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void upButtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upButtActionPerformed
        try {
            String bday = mmm.getSelectedItem().toString()
                            +" "+dd.getSelectedItem().toString()+" ,"+yyyy.getText();
            String fst = fname.getText();
            String first = fst.substring(0, 1).toUpperCase() + fst.substring(1);
            String mid = mname.getText();
            String middle = mid.substring(0, 1).toUpperCase() + mid.substring(1);
            String lst = lname.getText();
            String last = lst.substring(0, 1).toUpperCase() + lst.substring(1);
            
            String insert = "UPDATE employees SET id=?,lastname=?,firstname=?,middlename=?, "
                            + "gender=?,bday=?,program=?,email=?,phone=?,photo=?,bmonth=?,day=?,byear=? WHERE id = ?";
                    
            PreparedStatement pst = HRMS.getConnection().prepareStatement(insert);
            pst.setInt(1, Integer.parseInt(id.getText()));
            pst.setString(2, last);
            pst.setString(3, first);
            pst.setString(4, middle);
            pst.setString(5, gender.getSelectedItem().toString());
            pst.setString(6, bday);
            pst.setInt(7, prgrm.getSelectedIndex());
            pst.setString(8, eml.getText());
            pst.setString(9, phn.getText());
            pst.setString(10, path);
            pst.setString(11, mmm.getSelectedItem().toString());
            pst.setString(12, dd.getSelectedItem().toString());
            pst.setString(13, yyyy.getText());
            pst.setString(14, selectedProf);
            
            
            JLabel mess = new JLabel("Save changes?");
            Object[] opt = {"Yes", "No"};

            int n = JOptionPane.showOptionDialog(new JFrame(), mess, "",
                    JOptionPane.YES_NO_CANCEL_OPTION, 
                    JOptionPane.QUESTION_MESSAGE, null, opt, opt[0]);

            if (n==0) {
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Saved Successfully!");
                this.dispose();
            }

            if (n==1) {
                
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_upButtActionPerformed

