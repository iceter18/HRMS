    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        HRMS a = new HRMS();
        //JOptionPane.showMessageDialog(null, selectedProf);
        try {
            PreparedStatement ps = getConnection().prepareStatement("select * from employees where id = ?");
            ps.setString(1, selectedProf);
            ResultSet rs = ps.executeQuery();
            String program = null;
            
            while (rs.next()) {
 
                fname.setText(rs.getString("firstname"));
                mname.setText(rs.getString("middlename"));
                lname.setText(rs.getString("lastname"));
                id.setText(rs.getString("id"));
                mmm.setSelectedItem(rs.getString("bmonth"));
                dd.setSelectedItem(rs.getString("day"));
                yyyy.setText(rs.getString("byear"));
                gender.setSelectedItem(rs.getString("gender"));
                phn.setText(rs.getString("email"));
                eml.setText(rs.getString("phone"));
                prgrm.setSelectedIndex(rs.getInt("program"));
                path = rs.getString("photo");
                pic.setIcon(a.resizer(rs.getString("photo") , pic));
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formWindowOpened

